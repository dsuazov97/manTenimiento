    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        if( ckPantalla.isSelected() ||
            ckBateria.isSelected() ||
            ckPlaca.isSelected() ||
            ckCamara.isSelected() ||
            ckImei.isSelected()){
            
            String codigo,marca,modelo;
            int cantDias,precioBase;
            boolean cPantalla,cBateria,cPlaca,cCamara,cImei;
            Celular celu;
            
            codigo = VentIngreso.txtCodigo.getText();
            marca = VentIngreso.txtMarca.getText();
            modelo = VentIngreso.txtModelo.getText();
            cantDias = Integer.parseInt(VentIngreso.txtDias.getText());
            precioBase = Integer.parseInt(VentIngreso.txtPrecioBase.getText());
            cPantalla = ckPantalla.isSelected();
            cBateria = ckBateria.isSelected();
            cPlaca = ckPlaca.isSelected();
            cCamara = ckCamara.isSelected();
            cImei = ckImei.isSelected();
            
            celu = new Celular(codigo, marca, modelo, cantDias, precioBase, cPantalla, cBateria, cPlaca, cCamara, cImei);
            ManDAO prodDao = new ManDAO();
            if(prodDao.buscarCelu(codigo)==null && codigo.length()<=10 && cantDias<100 && cantDias>0 && precioBase>0){
                prodDao.ingresarCelular(celu);
                JOptionPane.showMessageDialog(this, "Celular Ingresado");
                VentIngreso.txtCodigo.setText(null);
                VentIngreso.txtMarca.setText(null);
                VentIngreso.txtModelo.setText(null);
                VentIngreso.txtDias.setText(null);
                VentIngreso.txtPrecioBase.setText(null);
                ckPantalla.setSelected(false);
                ckBateria.setSelected(false);
                ckPlaca.setSelected(false);
                ckCamara.setSelected(false);
                ckImei.setSelected(false);
                VentIngreso.txtCodigo.requestFocus();
                dispose();
                
            }else if (codigo.length()>10)
                JOptionPane.showMessageDialog(this, "El codigo debe ser menor a 10 digitos");
            else if(cantDias>99 || cantDias<1)
                JOptionPane.showMessageDialog(this, "Rango de Cant.Dias [1,99]");
            else if(precioBase<1)
                JOptionPane.showMessageDialog(this, "Precio Base mayor a 0");
            else{
                JOptionPane.showMessageDialog(this, "Codigo ya existente", 
                    "Â¡Â¡ Advertencia !!", JOptionPane.WARNING_MESSAGE);
                VentIngreso.txtCodigo.setText(null);
                VentIngreso.txtMarca.setText(null);
                VentIngreso.txtModelo.setText(null);
                VentIngreso.txtDias.setText(null);
                VentIngreso.txtPrecioBase.setText(null);
                ckPantalla.setSelected(false);
                ckBateria.setSelected(false);
                ckPlaca.setSelected(false);
                ckImei.setSelected(false);
                VentIngreso.txtCodigo.requestFocus();
            }
        }else{
        JOptionPane.showMessageDialog(this,"Seleccione algÃºn servicio");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed
    
