    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
    
        DefaultTableModel dtm=(DefaultTableModel)tblMostrar.getModel();
        int fila=tblMostrar.getSelectedRow();
        if(fila==-1)
            JOptionPane.showMessageDialog(this, "Debe seleccionar un celular");
        else{
            String codigo=String.valueOf(tblMostrar.getValueAt(fila,0));
            String marca=String.valueOf(tblMostrar.getValueAt(fila, 1));
            String modelo=String.valueOf(tblMostrar.getValueAt(fila, 2));
            int cantDias=Integer.parseInt(String.valueOf(tblMostrar.getValueAt(fila,3)));
            int precioBase=Integer.parseInt(String.valueOf(tblMostrar.getValueAt(fila,4)));
            boolean cPantalla=Boolean.parseBoolean((String) tblMostrar.getValueAt(fila, 5));
            boolean cBateria=Boolean.parseBoolean((String) tblMostrar.getValueAt(fila, 6));
            boolean cPlaca=Boolean.parseBoolean((String) tblMostrar.getValueAt(fila, 7));
            boolean cCamara=Boolean.parseBoolean((String) tblMostrar.getValueAt(fila, 8));
            boolean cImei=Boolean.parseBoolean((String) tblMostrar.getValueAt(fila, 9));
            
            Celular celu=new Celular(codigo, marca, modelo, cantDias, precioBase, cPantalla, cBateria, cPlaca, cCamara, cImei);
            ManDAO manDAO= new ManDAO();
            manDAO.modificarCelular(celu);
            JOptionPane.showMessageDialog(this,"Celular modificado");
        }
            
        // TODO add your handling code here:
    }//GEN-LAST:event_btnModificarActionPerformed

